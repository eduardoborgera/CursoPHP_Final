<?php
	
	$a = "7" == 7;  // Igual (tranforma para tipagens iguais e verifica valor)
	$b = 7 != 7;  // Diferente ou <>
	$c = "7" === 7; // Identico (verifica valor e tipagem)
	$d = 7 !== 7; // Nao Identico
	$e = 7 > 8;   // Maior
	$f = 7 < 8;   // Menor 
	$g = 7 <= 8;  // Menor ou igual
	$h = 7 >= 8;  // Maior ou igual

	var_dump($a);
	var_dump($b);
	var_dump($c);
	var_dump($d);
	var_dump($e);
	var_dump($f);
	var_dump($g);
	var_dump($h);