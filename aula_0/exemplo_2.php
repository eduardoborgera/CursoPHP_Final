<?php

	/*
	 * Operadores Matematicos Basicos
	 */

	//Declaracao de variaveis
	$a = 2;
	$b = 4;

	//Soma
	$soma = $a + $b;
	echo "$a + $b = " . $soma . "\n";

	//Subtracao
	$subtracao = $a - $b;
	echo "$a - $b = " . $subtracao . "\n";

	//Multiplicacao
	$multiplicacao = $a * $b;
	echo "$a x $b = " . $multiplicacao . "\n";

	//Divisao
	$divisao = $a / $b;
	echo "$a / $b = " . $divisao . "\n";